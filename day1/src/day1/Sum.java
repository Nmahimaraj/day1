package day1;

public class Sum {
	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 4;
		
		System.out.println(num1 + "+" + num2 + "=" +(num1 + num2));
		System.out.println(num1 + "-" + num2 + "=" +(num1 - num2));
		System.out.println(num1 + "*" + num2 + "=" +(num1 * num2));
		System.out.println(num1 + "/" + num2 + "=" +(num1 / num2));
		System.out.println(num1 + "mod" + num2 + "=" +(num1 % num2));
		
	}

}
