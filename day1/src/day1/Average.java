package day1;

public class Average {
	public static void main(String[] args) {
		int input1 = 20;
		int input2 = 89;
		int input3 = 34;
		int input4 = 43;
		int input5 = 78;
		int total = 5;

		System.out.println("Average of five numbers :" + (input1 + input2 + input3 + input4 + input5) / total);

	}


}
